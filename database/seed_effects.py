async def _upsert_effects(db_path: str) -> None:
    async with aiosqlite.connect(db_path) as db:
        for eff in EFFECTS:
            prepared_prompt = _ensure_gender_variants_prompt(eff['prompt'])
            demo_file_id = _portable_demo(eff.get('demo_file_id'))
            demo_type = eff.get('demo_type') if demo_file_id else None
            cur = await db.execute('SELECT id FROM effects WHERE button_name = ? AND type = ?', (eff['button_name'], eff['type']))
            row = await cur.fetchone()
            if row:
                if demo_file_id:
                    await db.execute(
                        'UPDATE effects SET prompt = ?, demo_file_id = ?, demo_type = ?, sort_order = ?, is_active = ?, type = ? WHERE id = ?',
                        (prepared_prompt, demo_file_id, demo_type, eff['sort_order'], eff['is_active'], eff['type'], row[0]),
                    )
                else:
                    await db.execute(
                        'UPDATE effects SET prompt = ?, sort_order = ?, is_active = ?, type = ? WHERE id = ?',
                        (prepared_prompt, eff['sort_order'], eff['is_active'], eff['type'], row[0]),
                    )
            else:
                await db.execute(
                    'INSERT INTO effects (button_name, prompt, demo_file_id, demo_type, type, is_active, sort_order, created_at) VALUES (?, ?, ?, ?, ?, ?, ?, datetime("now"))',
                    (eff['button_name'], prepared_prompt, demo_file_id, demo_type, eff['type'], eff['is_active'], eff['sort_order']),
                )
        await db.commit()


async def main() -> None:
    db_path = load_config().database_path
    await setup(db_path)
    await _upsert_effects(db_path)


if __name__ == '__main__':
    asyncio.run(main())
