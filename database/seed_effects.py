def _ensure_gender_variants_prompt(prompt: str) -> str:
    clean = (prompt or "").strip()
    if not clean:
        return clean
    if clean.startswith(OLD_PREFIX_MARKER):
        parts = clean.split("\n\n", 1)
        if len(parts) == 2:
            clean = parts[1].strip()
    if "\u0415\u0441\u043b\u0438 \u043d\u0430 \u0444\u043e\u0442\u043e \u043c\u0443\u0436\u0447\u0438\u043d\u0430:" in clean:
        return clean
    return f"{clean}{GENDER_VARIANTS_SUFFIX}"


def _portable_demo(value: str | None) -> str | None:
    if not value:
        return None
    if value.startswith(("http://", "https://")):
        return value
    if Path(value).exists():
        return value
    return None

